export function AppSidebar({ ...props }: React.ComponentProps<typeof Sidebar>) {
  const { activeSection, setActiveSection } = useSidebarContext()

  const handleCloudItemClick = (title: string) => {
    const sectionMap: Record<string, any> = {
      'Capture': 'capture',
      'Proposal': 'proposal',
      'Prompts': 'prompts',
    }
    
    const section = sectionMap[title]
    if (section) {
      setActiveSection(section)
    }
  }

  const getActiveSectionFromTitle = (title: string) => {
    const sectionMap: Record<string, any> = {
      'Capture': 'capture',
      'Proposal': 'proposal',
      'Prompts': 'prompts',
    }
    return sectionMap[title] || ''
  }

  return (
    <Sidebar collapsible="offcanvas" {...props}>
      <SidebarHeader>
        <SidebarMenu>
          <SidebarMenuItem>
            <SidebarMenuButton
              asChild
              className="data-[slot=sidebar-menu-button]:!p-1.5"
            >
              <a href="#">
                <IconInnerShadowTop className="!size-5" />
                <span className="text-base font-semibold">Agent Flow.</span>
              </a>
            </SidebarMenuButton>
          </SidebarMenuItem>
        </SidebarMenu>
      </SidebarHeader>
      <SidebarContent>
        <NavMain items={data.navMain} />
        
        {/* Cloud Navigation */}
        <SidebarGroup>
          <SidebarGroupLabel>Workflow</SidebarGroupLabel>
          <SidebarMenu>
            {data.navClouds.map((item) => (
              <SidebarMenuItem key={item.title}>
                <SidebarMenuButton 
                  tooltip={item.title}
                  onClick={() => handleCloudItemClick(item.title)}
                  className={activeSection === getActiveSectionFromTitle(item.title) ? 'bg-accent' : ''}
                >
                  {item.icon && <item.icon />}
                  <span>{item.title}</span>
                </SidebarMenuButton>
              </SidebarMenuItem>
            ))}
          </SidebarMenu>
        </SidebarGroup>
        
        <NavDocuments items={data.documents} />
        <NavSecondary items={data.navSecondary} className="mt-auto" />
      </SidebarContent>
      <SidebarFooter>
        <NavUser user={data.user} />
      </SidebarFooter>
    </Sidebar>
  )
}
